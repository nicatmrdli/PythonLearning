
friends = ["Kevin", "Karen", "Jim", "Oscar", "Toby"]
# Use [x, y, z] to create a list

print(friends[0])
# This will access the the input that you selected. in this case its Kevin.

print(friends[-1])
# You can also use negative value to start the count from the right side.

print(friends[1:4])
# This will selected the specific range of string values on the list. In this case its Karen, Jim and Oscar.

friends[1] = "Nicat"
print(friends[1])
# You can use this code to modify the selected string with a new string. In this case we replaced Karen with Nicat